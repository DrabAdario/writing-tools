const DEFAULT_THEME: ElementTheme = {
  border: "border-zinc-600",
  shadow: "shadow-zinc-900/50",
  cardAccent: "ring-1 ring-zinc-500/20",
  overlayBackdrop: "from-zinc-950/95 via-zinc-900/40 to-transparent",
  overlayLabel: "text-zinc-100 drop-shadow-lg",
};

const RARITY_GLASS: Record<RarityTier, string> = {
  common: "",
  uncommon: "brightness-[1.03]",
  rare: "brightness-[1.06] saturate-[1.08]",
  epic: "brightness-[1.1] saturate-[1.12]",
};

const RARITY_RING: Record<RarityTier, string> = {
  common: "",
  uncommon: "ring-1 ring-white/5",
  rare: "ring-1 ring-white/10",
  epic: "ring-2 ring-white/15",
};

const RARITY_SHADOW: Record<RarityTier, string> = {
  common: "",
  uncommon: "shadow-lg",
  rare: "shadow-xl",
  epic: "shadow-2xl",
};

export function getElementTheme(element: string, rarity: RarityTier = "common"): ElementTheme {
  const base = ELEMENT_THEMES[element] ?? DEFAULT_THEME;
  const glass = RARITY_GLASS[rarity];
  const ring = RARITY_RING[rarity];
  const sh = RARITY_SHADOW[rarity];

  return {
    ...base,
    shadow: `${base.shadow} ${sh}`.trim(),
    cardAccent: [base.cardAccent, ring, glass].filter(Boolean).join(" "),
  };
}
